"""Logging configuration."""

import sys
from loguru import logger

def setup_logger():
    """Configure logger."""
    logger.remove()
    logger.add(
        sys.stdout,
        format="<green>{time:HH:mm:ss}</green> | <level>{level: <8}</level> | <cyan>{name}</cyan>:<cyan>{function}</cyan> - <level>{message}</level>",
        level="INFO",
        colorize=True,
    )
    return logger

log = setup_logger()
